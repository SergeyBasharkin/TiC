package myArithmeticCoding;

public class SegmentFloat {

    private Float left;
    private Float right;

    public SegmentFloat(Float left, Float right) {
        this.left = left;
        this.right = right;
    }

    public Float getLeft() {
        return left;
    }

    public void setLeft(Float left) {
        this.left = left;
    }

    public Float getRight() {
        return right;
    }

    public void setRight(Float right) {
        this.right = right;
    }
}
